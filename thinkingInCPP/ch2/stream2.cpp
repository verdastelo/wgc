#include <iostream>
using namespace std;

int main() {
  cout << "this is 15 in decimal " << dec << 15 << endl;
  cout << "this is 15 in octal " << oct << 15 << endl;
  cout << "this is 1979 in hexadecimal " << hex << 1979 << endl;
  cout << "this is a floating-point number " << 3.14 << endl;
  cout << "an example of casting " << char (65) << endl;
  return 0;
}
